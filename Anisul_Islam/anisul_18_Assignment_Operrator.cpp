#include <conio.h>
using namespace std;
int main ()
{
    int x =10;
    x+=2;
    cout<<"x+=2 : "<<x<<endl; // x=x+2; x=10+2; x=12
    x-=2;
    cout<<"x-=2 : "<<x<<endl; // x= x-2; x=12-2; x=10;
    x*=2;
    cout<<"x*=2 : "<<x<<endl; // x=x*2;  x=10*2; x=20;
    x/=2;
    cout<<"x/=2 : "<<x<<endl; // x=x/2;  x=20/2; x=10
    x%=2;
    cout<<"x%=2 : "<<x<<endl; // x=x%2;  x=10/2; x=0 ভাগশেষ শূণ্য



    getch();
}
