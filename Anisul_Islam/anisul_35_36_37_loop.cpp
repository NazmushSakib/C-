#include<bits/stdc++.h>
using namespace std;
int main()
{
    for(int i=1;i<=10;i++) 
    {
        cout <<"Bangladesh = " <<i<<endl;
    }

    cout <<"End of For Loop"<<endl;

    int j=1;
    while(j<=10)
    {
        cout <<"Dhaka = "<<j<<endl;
        j++;
    }    

    cout <<"End of While Loop"<<endl;

    int k = 1;
    do
    {
        cout<<"Moghbazar = "<<k<<endl;
        k++;
    } 
    while (k<=10);
    

    cout<<"End of Do-While loop"<<endl;

    return 0;
}